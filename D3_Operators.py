#Arithmetic Operations and assigning the result to the variable
a = 3  # a is a variable name and 3 is an integer data type
b = 2  # b is a variable name and 2 is an integer data type

total = a + b  
diff = a - b
product = a * b
division = a / b
remainder = a % b
floor_division = a // b
exponential = a ** b

print(total)
